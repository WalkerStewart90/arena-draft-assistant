import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const jsonPath = path.join(root, 'data', 'sosPremierPickStats.json')
const outPath = path.join(root, 'src', 'data', 'sosPremierPickStats.generated.ts')

if (!fs.existsSync(jsonPath)) {
  console.error('Missing data/sosPremierPickStats.json — run:')
  console.error('  node scripts/aggregate-sos-premier-picks.mjs <path-to-premier-draft.csv>')
  process.exit(1)
}

const payload = JSON.parse(fs.readFileSync(jsonPath, 'utf8'))
const cards = payload.cards ?? []

let ts = `// Generated by scripts/generate-sos-pick-data.mjs — do not edit by hand.\n`
ts += `export const SOS_PREMIER_META = ${JSON.stringify(
  {
    generatedAt: payload.generatedAt,
    sourceFile: payload.sourceFile,
    rowsProcessed: payload.rowsProcessed,
    totalPicks: payload.totalPicks,
    uniqueCards: payload.uniqueCards,
  },
  null,
  2,
)} as const\n\n`
ts += `export interface SosPremierPickStat {\n`
ts += `  displayName: string\n`
ts += `  pickCount: number\n`
ts += `  avgPickNumber: number\n`
ts += `  avgPackNumber: number\n`
ts += `  pickRate: number\n`
ts += `}\n\n`
ts += `export const SOS_PREMIER_PICK_STATS: Record<string, SosPremierPickStat> = {\n`

for (const row of cards) {
  const key = row.displayName.trim().toLowerCase()
  ts += `  ${JSON.stringify(key)}: { displayName: ${JSON.stringify(row.displayName)}, pickCount: ${row.pickCount}, avgPickNumber: ${row.avgPickNumber}, avgPackNumber: ${row.avgPackNumber}, pickRate: ${row.pickRate} },\n`
}

ts += `}\n`

fs.mkdirSync(path.dirname(outPath), { recursive: true })
fs.writeFileSync(outPath, ts, 'utf8')
console.log(`Wrote ${cards.length} SOS Premier pick stats to ${path.relative(root, outPath)}`)
