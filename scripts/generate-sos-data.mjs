import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const csvPath = path.join(root, 'data', 'SOS.csv')
const outPath = path.join(root, 'src', 'data', 'sosGiwr.generated.ts')

function parseCsvLine(line) {
  const out = []
  let cur = ''
  let q = false
  for (let i = 0; i < line.length; i++) {
    const ch = line[i]
    if (ch === '"') {
      q = !q
      continue
    }
    if (ch === ',' && !q) {
      out.push(cur)
      cur = ''
      continue
    }
    cur += ch
  }
  out.push(cur)
  return out
}

function parsePercent(value) {
  const m = String(value).trim().match(/^([\d.]+)\s*%?$/)
  return m ? Number.parseFloat(m[1]) : Number.NaN
}

function parseCount(value) {
  const n = Number.parseInt(String(value).replace(/,/g, '').trim(), 10)
  return Number.isFinite(n) ? n : 0
}

if (!fs.existsSync(csvPath)) {
  console.error('Missing data/SOS.csv — copy SOS stats CSV into draft-assistant/data/')
  process.exit(1)
}

const text = fs.readFileSync(csvPath, 'utf8').replace(/^\uFEFF/, '')
const lines = text.split(/\r?\n/).filter((line) => line.length > 0)
const header = parseCsvLine(lines[0]).map((cell) => cell.trim())

function colIndex(names) {
  for (const name of names) {
    const i = header.indexOf(name)
    if (i >= 0) {
      return i
    }
  }
  return -1
}

const idxName = colIndex(['Name'])
const idxGihWr = colIndex(['GIH WR'])
const idxNGih = colIndex(['# GIH', '#GIH'])

if (idxName < 0 || idxGihWr < 0 || idxNGih < 0) {
  console.error('SOS.csv must include columns: Name, GIH WR, # GIH')
  process.exit(1)
}

/** @type {Map<string, { displayName: string; gihWrPercent: number; nGih: number }>} */
const map = new Map()

for (let i = 1; i < lines.length; i++) {
  const cols = parseCsvLine(lines[i])
  if (cols.length <= Math.max(idxName, idxGihWr, idxNGih)) {
    continue
  }
  const displayName = cols[idxName]?.trim()
  if (!displayName) {
    continue
  }
  const gihWrPercent = parsePercent(cols[idxGihWr])
  const nGih = parseCount(cols[idxNGih])
  if (!Number.isFinite(gihWrPercent)) {
    continue
  }
  const key = displayName.toLowerCase()
  const prev = map.get(key)
  const row = { displayName, gihWrPercent, nGih }
  if (!prev || row.nGih > prev.nGih) {
    map.set(key, row)
  }
}

let ts = `// Generated by scripts/generate-sos-data.mjs from data/SOS.csv — do not edit by hand.\n`
ts += `export interface SosCardStat {\n  displayName: string\n  gihWrPercent: number\n  nGih: number\n}\n\n`
ts += `export const SOS_CARD_STATS: Record<string, SosCardStat> = {\n`

for (const [, row] of map) {
  const key = row.displayName.toLowerCase()
  ts += `  ${JSON.stringify(key)}: { displayName: ${JSON.stringify(row.displayName)}, gihWrPercent: ${row.gihWrPercent}, nGih: ${row.nGih} },\n`
}

ts += `}\n`

fs.mkdirSync(path.dirname(outPath), { recursive: true })
fs.writeFileSync(outPath, ts, 'utf8')
console.log(`Wrote ${map.size} SOS cards to ${path.relative(root, outPath)}`)
